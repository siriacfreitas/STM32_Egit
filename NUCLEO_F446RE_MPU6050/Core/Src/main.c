/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  I2C_ScanBus(&hi2c1, &huart2);
  MPU6050_Check(&hi2c1, &huart2);
  MPU6050_SetAccelRange_2g(&hi2c1);
  MPU6050_SetGyroRange_250dps(&hi2c1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	    UART_SendStrln (&huart2,"----------------------------------------");

	    if (MPU6050_ReadAccelRaw(&hi2c1, &ax, &ay, &az) == HAL_OK) {

	        MPU6050_AccelRawTo_g_2g(ax, ay, az, &axg, &ayg, &azg);
	        //UART_Printf(&huart2, "ACC raw: %6d %6d %6d | ", ax, ay, az);
	        UART_SendStr(&huart2, "g: ");
	        UART_SendFloat3(&huart2, axg); UART_SendStr(&huart2, " ");
	        UART_SendFloat3(&huart2, ayg); UART_SendStr(&huart2, " ");
	        UART_SendFloat3(&huart2, azg); UART_SendStrln(&huart2, "");

	    } else {
	        UART_SendStrln(&huart2, "Falha leitura ACC");
	    }

	    HAL_Delay(100);


	    if (MPU6050_ReadTemp_centiC(&hi2c1, &t_centi) == HAL_OK) {
	        int neg = (t_centi < 0);
	        int16_t mag = neg ? (int16_t)(-t_centi) : t_centi;   // magnitude positiva
	        UART_Printfln(&huart2, "Temp: %s%d.%02d C",
	                      neg ? "-" : "", mag/100, mag%100);
	    } else {
	        UART_SendStrln(&huart2, "Falha leitura temperatura");
	    }

	    if (MPU6050_ReadGyroRaw(&hi2c1, &gx, &gy, &gz) == HAL_OK) {
	        MPU6050_GyroRawTo_cdegps_250dps(gx, gy, gz, &gx_cdps, &gy_cdps, &gz_cdps);

	        /* Envia bruto e convertido (centi-deg/s) via UART (sem %f) */
	        //UART_Printfln(&huart2, "GYR raw: %6d %6d %6d | " ,gx, gy, gz);
	        UART_Printfln(&huart2, "cdps: %6d %6d %6d",gx_cdps, gy_cdps, gz_cdps);
	    } else {
	        UART_SendStrln(&huart2, "Falha leitura GYRO");
	    }

	    UART_SendStrln (&huart2,"----------------------------------------");
	    HAL_Delay(2000);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

